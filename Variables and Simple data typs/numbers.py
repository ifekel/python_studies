# Integers
# You can add (+), subtract (-), multiply (*), and divide (/) integers in Python
print(2 + 2)
print(3 - 1)
print(2 * 5)
print(10 / 5)

# Exponential
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)

# Order of evaluation
print(2 + 3 * 4)
print((2 + 4) * 4)

# Floats
print(0.1 + 0.2)
print(0.2 * 0.1)

# Integers and Floats
print(4/2)
print(1 + 2.3)
print(2 * 3.2)
print(3.0 ** 2)

# Multiple Assignments
x, y, z = 12, 5,0
print(x)
print(y)
print(z)

# Constants
MAX_CONNECTION = 500
print(MAX_CONNECTION)


