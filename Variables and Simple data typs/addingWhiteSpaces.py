print("python", "(With whitespace:)")
print("\tpython", "(With whitespace: )")

# Adding new line
print("languages: \n1.Python \n2.C \n3.JavaScript")

# Combining tabs and whitespaces
print("languages: \n\t1.C\n\t2.Javascript\n\t3.Python")

# Stripping whitespaces
favorite_language = "   python"
print(favorite_language)

favorite_language = favorite_language.strip()
